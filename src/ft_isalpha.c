#include "libft.h"
#include <stdio.h>

int		ft_isalpha(int c)
{
	unsigned char cc;

	cc = c;
	if ((cc >= 65 && cc <= 90) || (cc >= 97 && cc <= 122))
		return (1);
	return (0);
}
